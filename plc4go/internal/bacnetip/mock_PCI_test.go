// MockPCI_Update_Call is a *mock.Call that shadows Run/Return methods with type explicit version for method 'Update'
type MockPCI_Update_Call struct {
	*mock.Call
}

// Update is a helper method to define mock.On call
//   - pci Arg
func (_e *MockPCI_Expecter) Update(pci interface{}) *MockPCI_Update_Call {
	return &MockPCI_Update_Call{Call: _e.mock.On("Update", pci)}
}

func (_c *MockPCI_Update_Call) Run(run func(pci Arg)) *MockPCI_Update_Call {
	_c.Call.Run(func(args mock.Arguments) {
		run(args[0].(Arg))
	})
	return _c
}

func (_c *MockPCI_Update_Call) Return(_a0 error) *MockPCI_Update_Call {
	_c.Call.Return(_a0)
	return _c
}

func (_c *MockPCI_Update_Call) RunAndReturn(run func(Arg) error) *MockPCI_Update_Call {
	_c.Call.Return(run)
	return _c
}

// NewMockPCI creates a new instance of MockPCI. It also registers a testing interface on the mock and a cleanup function to assert the mocks expectations.
// The first argument is typically a *testing.T value.
func NewMockPCI(t interface {
	mock.TestingT
	Cleanup(func())
}) *MockPCI {
	mock := &MockPCI{}
	mock.Mock.Test(t)

	t.Cleanup(func() { mock.AssertExpectations(t) })

	return mock
}
